let a = 10;
let b = 5;
let c = 20;
let d = 7;
let e = 3;

// Arithmetic operators

let sum = a + b + c + d + e;
let product = a * b * e ;
let average = sum / 5;
let remainder = c % e;
let power = b ** 2;

console.log(sum);
console.log(product);
console.log(average);
console.log(remainder);
console.log(power);


// Comparison operators

let isgreater = c > a;
let isequal = b == e;
let strictequal = b === 5;
let notequal = d != a;

console.log(isgreater);
console.log(isequal);
console.log(strictequal);
console.log(notequal);


// Logical operators

let logic1 = (a > b) && (c > d);
let logic2 = (a < b) || (c > d);
let logic3 = !(b == 5);

console.log(logic1);
console.log(logic2);
console.log(logic3);


// Assignment operators

let x = a;
x += b;
x *= e;
x -= d;

console.log(x);
console.log(a);
console.log(b);
console.log(d);




